let array=[10,20,30,40,50,60]

//push method => add element at last
array.push(70)
console.log(array)

//pop method => remove element from last
array.pop()
console.log(array)

//shift method => remove element from first
array.shift()
console.log(array)

//unshift method => add element at first
array.unshift(5)
console.log(array)
